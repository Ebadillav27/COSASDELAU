package progra1;

public class caracteres
{
    public static void main(String[] args)
    {

        String nombre = "Ben";
        
        String apellido = "Kenobi"; 

        System.out.println();
        System.out.println("Concatenacion: \n" + nombre + " \t" + apellido);
        
        System.out.println("backspace: \b" + nombre + " " + apellido);
        System.out.println("retorno: \r" + nombre + " " + apellido);
        System.out.println("comilla simple: \'" + nombre + " " + apellido + "\'");
        System.out.println("comilla doble: \"" + nombre + " " + apellido + "\"");
    }



}